import Foundation
import UIKit

extension UIColor {
    
    class func zendeskBlue() -> UIColor {
        return UIColor(red: 4/255, green: 54/255, blue: 61/255, alpha: 1)
    }
    
    class func darkGray()-> UIColor  {
        return UIColor(red: 169/255, green: 169/255, blue: 169/255, alpha: 1)
    }
}
